USE gym;

-- Pessoa(SSN(nn), Nome(nn), Data de Nascimento(nn), Email(nn), Rua(nn), Bairro(nn), Cidade(nn), Número-Complemento(nn), FuncFlag(nn), Salário, AlunFlag, Altura, Peso, TreinadorSSN)
CREATE TABLE IF NOT EXISTS Pessoa(
	SSN char(11) not null,
	Nome varchar(80) not null,
	DataDeNascimento date not null,
	Email varchar(40) not null, 
	Cidade varchar(20) not null,
	Rua varchar(50) not null, 
	FuncFlag boolean not null, 
	Salario double, 
	AlunFlag boolean not null, 
	Altura double, 
	Peso double, 
	TreinadorSSN char (11),
	PRIMARY KEY (SSN),
	FOREIGN KEY (TreinadorSSN) REFERENCES Pessoa(SSN)
);

-- Ficha_de_Exercicio(SSN, ID, Repetições, Descanso)
CREATE TABLE IF NOT EXISTS FichaDeExercicio(
	SSNAluno char(11) not null,
	ID integer not null,
	Repeticoes integer, 
	Descanso integer,
	PRIMARY KEY (ID),
	FOREIGN KEY (SSNAluno) REFERENCES Pessoa(SSN) ON DELETE CASCADE
);

-- Exercicio(Nome(nn), Tipo, Aparelho)
CREATE TABLE IF NOT EXISTS Exercicio(
	Nome varchar(30) not null, 
	Tipo varchar(20) not null, 
	Aparelho varchar(20) not null,
	PRIMARY KEY (Nome)
);

-- Telefone(SSN(nn), Número(nn))
CREATE TABLE IF NOT EXISTS Telefone(
	SSN char(11) not null, 
	Numero char(12) not null,
	PRIMARY KEY (SSN, Numero),
	FOREIGN KEY (SSN) REFERENCES Pessoa(SSN) ON DELETE CASCADE
);

-- Modalidade(Nome(nn), Horário(nn), Sala(nn))
CREATE TABLE IF NOT EXISTS Modalidade(
	ID integer not null,
	Nome char(20) not null, 
	Horario time not null, 
	sala varchar(10) not null,
	PRIMARY KEY (ID)
);

-- Pagamento(SSN(nn), Data(nn), Valor(nn), Forma de pagamento(nn)
CREATE TABLE IF NOT EXISTS Pagamento(
	SSN char(11) not null, 
	Data date not null, 
	Valor double not null, 
	FormaDePagamento varchar (20) not null,
	Pago boolean not null,
	PRIMARY KEY (SSN,Data),
	FOREIGN KEY (SSN) REFERENCES Pessoa(SSN)
);

-- Ensina(SSNProf(nn), NomeMod(nn), Horário(nn), SSNAluno(nn))
CREATE TABLE IF NOT EXISTS Ensina(
	SSNProf char(11) not null, 
	IDMod integer not null, 
	SSNAluno char (11) not null,
	PRIMARY KEY (SSNProf, IDMod, SSNAluno),
	FOREIGN KEY (SSNProf) REFERENCES Pessoa(SSN),  
	FOREIGN KEY (SSNAluno) REFERENCES Pessoa(SSN)ON DELETE CASCADE,
	FOREIGN KEY (IDMod) REFERENCES Modalidade(ID) ON DELETE CASCADE
);

-- Ficha Contém(ID, NomeExercicio(nn))
CREATE TABLE IF NOT EXISTS FichaContem(
	ID integer not null,
	NomeExercicio varchar(30) not null,
	PRIMARY KEY (ID, NomeExercicio),
	FOREIGN KEY (ID) REFERENCES FichaDeExercicio(ID) ON DELETE CASCADE,
	FOREIGN KEY (NomeExercicio) REFERENCES Exercicio(Nome) ON DELETE CASCADE
);

